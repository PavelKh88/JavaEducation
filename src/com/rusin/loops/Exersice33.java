package com.rusin.loops;

public class Exersice33 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10 ; i++) {

            System.out.print(" " + Math.pow(i,2) + " ");
        }
        System.out.println();
        for (int i = 10; i >= 1 ; i--) {

            System.out.print(" " + ( int )Math.pow(i,3) + " ");
        }
    }
}
